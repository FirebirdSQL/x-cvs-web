<h1>Firebird Logos</h1>

<p>This is a collection of the basic official Firebird logo in various formats. The bitmaps are 
in 24-bit PNG format with white background, so you should be able to convert 
them into anything you like.</p> 

<p>These renderings were prepared for the Firebird Project by Stefan Heymann.  The Firebird trademarks and logos are owned by the Firebird Foundation Inc., on behalf of the Firebird Project.  The images may be freely used for any legitimate purpose related to the Firebird open source database software.</p>
<blockquote>
<p>For a selection of &quot;gloat logos&quot; (with captions like &quot;Powered by Firebird&quot;, et al.) link <a href="index.php?op=ffoundation&id=gloat_logos" style="text-decoration:none;color:maroon;"><b> H E R E </b></a>.</p>
</blockquote>

<p>The logo comes in two versions: with and without a drop-shadow. The  
version without the drop-shadow is the &quot;official version&quot; that was the winner of the original competition in 2001.  The version with drop-shadow was offered soon after by Markus Soell, to provide better contrast on light-coloured backgrounds.</p>

