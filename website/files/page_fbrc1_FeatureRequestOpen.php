- fix 666 (or 777) modes on some files.<br>
- create directory /var/interbase (or something like that) where .gdb files will be stored (or somewhere in ~interbase)<br>
<br>
Somewhere deep in server code:<br>
- check which will prevent InterBase daemon from as root<br>
