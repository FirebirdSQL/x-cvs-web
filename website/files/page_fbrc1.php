<H4>Firebird 1.00 Release Candidate 1 (RC1)</H4>
<P>Well, we have finally got this far. This Release Candidate represents the
build that we want to release as 1.0. This is scheduled to be the next main
release, and provided we don't find any critical bugs with this code, it will
be officially made available as 1.0 shortly. This release was built from code
which has been subjected to extensive cleanup, bug-fixing and beta testing
during the year between the original release of the code and this release. The
On-Disk Structure is still set at ODS 10. Certain enhancements requiring
changes to the ODS have been deferred to a future release. </P>
