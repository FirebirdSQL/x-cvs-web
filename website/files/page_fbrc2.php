<H4>Firebird 1.00 Release Candidate 2 (RC2)</H4>
<P>This is our second release candidate, and builds upon release candidate 1.0.
We are still moving towards a full release of Firebird 1.0, hopefully in the
new year. This release also includes the binaries for 64bit I/O support.
Packages with 64bit I/O support allow for single database files greater than
2GB. Bugs found during the RC1 phase have been fixed in RC2 (see the FAQ for
details).</P>
