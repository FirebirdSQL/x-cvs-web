<LI><a href="http://www.borland.com/about/cases/studies/bawns.html">National Semiconductor</a><BR>
Many years ago, when National Semiconductor first commenced manufacturing, its facilities were centralized. Design,
                           engineering, production and shipping were all located in Santa Clara, CA. To store critical information about production
                           specifications and manufacturing processes for each assembly, production engineers created a manual binder system. <P>Over
                           time, as products and processes matured, an enormous volume of information was accumulated in these binders—including
                           technical specifications, assembly diagrams, marketing materials, letters, photographs, customer data and quality control
                           sheets. Although some structured data was stored in legacy mainframe databases, the binders were the only central repository
                           for all of the information on a specific product or line. 
<P>
In a creative approach using <B>InterBase</B> and the World Wide Web, National Semiconductor found a way to create a
                           virtual binder to service its rapidly expanding field organization. 
