<li><a href="http://www.borland.com/about/cases/studies/treasury.html">UK Treasury</a><br>

The UK Treasury needed to improve its ability to access economic modeling data kept on its mainframe system. 

Introducing a PC-based office automation system, the Treasury decided to phase out the mainframe and move all its statistical applications to a client/server platform, choosing <B>InterBase</B> as the database server for the Delphi analysis engine. 
<P>
The client/server system proved equal in reliability to the mainframe, while being much faster, and far less costly to maintain.<p>

