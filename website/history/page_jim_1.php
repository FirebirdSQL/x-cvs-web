This had Don, Ann, Dave, Hector, Cassandra, and myself working out of
what had become known as the Reedy Meadow Research Center.  To reclaim
our house, we moved to Tynsborough, MA, over a pizza shop.  Bad pizza,
but we got the house back.
<p>
Moving forward, we conned Kathleen Sullivan, another DEC crony, to join
as Support Czar, Larry Weinszczak (a.k.a. Larry Wiggle) as head of sales,
and Maggie Hinkle, in charge of everything else.  Now we were seven.<P>
Next came the name change.  We made a list of names we liked, and picked
a syllable from column a, one from column b, and one from column c and
got Interbase.  Don and Ann cooked up the dumb schema of the company
being Inter<b>b</b>ase and the product Inter<b>B</b>ase, over my objections.  We eventually
got an Esther Dyson award for flaky capitalization.<P>
Intimate has its advantages, but lucrative isn't among them.  We went
looking for partners.  We found Cognos and Ashton Tate found us.  Dazzled
by California ****** we went with Ashton Tate.  We were looking for instant
credibility.  Little did we know that two years later they would be looking
to us for credibility.  Strange place, California.<P>
Ashton Tate exercised an option to buy the company in 1991.  Ashton Tate
planned to leave Interbase where is, as is, but with a new president.
(I bowed out due to philosophical disagreements with A-T's president.
Or was it to spend more time with my family?  Pursue other interests?)
In any case, my last official act was to sign both sides of my separation
agreement.<P>
Shortly after the acquisition, Borland ate Ashton Tate, ergo Interbase.
That, however, happened on somebody else's shift.
