The company was originally
called Groton Database Systems after my then town, Groton, MA.  That
is where the ubiquitous &quot;gds&quot; comes from.  We had no intention of leaving
the company with that name, but the first three good names failed name
search, and I got tired of paying for searches.<P>
I started the company
in the second floor spare bedroom working on a Pro-350 (a bastard pdp-11)
running something called zenix.<P>
I started alone, but was soon joined by Don DePalma, a long term DEC
crony and cat hater, and two Siamese cats, Cassandra and Hector.  I
wrote the code, Don wrote the books and tried to lure the cats off the balcony.<P>
Our first major deal was with Apollo.  The plan was that they would
private-label the product, peddle it for us, and send big checks.<P>
To keep me honest, they held my wife (Ann Harrison) hostage.  I delivered
the beta version of the product about a year after startup (not bad,
Virginia).  After a successful external Beta, Apollo suffered a corporate
hiccup, reorganized, got a new president, a new business plan, and out
of the private-label software biz.  Gulp.<P>
Ann had been working for a fellow named Dave Root at Apollo.  Post-hiccup,
he found himself manager sans department and defected to Groton Database
Systems.  Being otherwise decent folks, the Apollo guys also released
Ann from hostage escrow, and she joined too.<P>

