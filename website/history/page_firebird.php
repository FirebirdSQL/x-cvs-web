<H4>Firebird records</H4>
The InterBase community's activity since the July 25 opening of the InterBase® sources has been frenetic. Within 24 hours, a source code tree was up. By the end of the first weekend, the Firebird project on SourceForge began. Since then, projects have sprouted from every quarter, as the community launched into the initial wetting of hands.<p>

Unwilling to wait for Inprise to resolve difficulties resulting from the breakdown of negotiations to spin off a support company for OS InterBase®, open source coders set up their Firebird tree as a workshop repository for initial work on platform builds, new ports and tools developments.<p>

At the beginning, Firebird developers were used to use <A href="www.mers.com">MER Systems</A> News Server (because there was also the famous InterBase list) as main communication HUB. But because Robert Schieck, the owner of MERS server joined the Borland camp (he is now the designated <A href="http://community.borland.com/interbase">InterBase Community</A> Leader at Borland) and started to moderate some community members down, all communication moved to other places, like yahoogroups (egroups at that time) and SourceForge, and importance of this server declined (InterBase list at MER Systems is now defunct, but searchable archive is still available).<p>

Since it's conception, the evolution of Firebird Project was roaring. Unfortunately, the pace of changes was so high that many historical records and documents from first year are lost or buried deeply in personal archives. Anyway, here is the list of some events in history of Firebird project:<p>

