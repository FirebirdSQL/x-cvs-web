<H5><a name="contribute"></a>Contribute to the Firebird Core Engine Project</H5>

If you want to join the Firebird team you have to do next things:<br>
<ol>
  <li>Take your time to watch the traffic in <a href="index.php?op=lists#fb-devel" target="_blank" title="Firebird-devel Mailinglist">&quot;firebird-devel&quot;</a> mailing list for awhile until you will not get comfortable with our development efforts, structure, process and future plans.</li>
  <li>Choose your working area:
    <ul>
      <li>You can join one of our development drifts.</li>
      <li>You can <A href="index.php?op=devel&id=bugdb">choose a bug</A> to fix it.</li>
      <li>You can propose and design a new feature. It's better to consult it in <A href="index.php?op=lists#ib-architect">IB-Architect</A> mailing list first.</li>
    </ul>
  </li>
  <li>
    Post in the <a href="index.php?op=lists#fb-devel" target="_blank" title="Firebird-devel Mailinglist">&quot;firebird-devel&quot;</a> mailing list about your intention, area of development and how you want to implement it.</li>
  <li>Ask for code review for your patch.</li>
  <li>If you have at least one patch accepted, and you want to continue your work on Firebird Core Engine, you can ask for CVS write access.</li>
</ol>

