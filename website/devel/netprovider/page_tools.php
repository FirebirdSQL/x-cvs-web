<h4>Firebird ADO.NET Data Provider - Related tools</h4>

<h3>Open Source</h3>

<h1><a href="nhibernate.sourceforge.net">NHibernate</a></h1>
<p>
"NHibernate is a .NET based object persistence library for relational databases. 
NHibernate is a port of the excellent Java Hibernate relational persistence tool."
</p>

<h1><a href="http://www.mertner.com/confluence/display/Gentle/Home">Gentle.NET</a></h1>
<p>
"Gentle.NET is an RDBMS independent object persistence framework. It features automatic 
SQL generation and object construction, an SQL factory for creating custom queries, 
DataView construction helpers, excellent performance and reasonably complete docs."
</p>

<h1><a href="http://neo.codehaus.org/">NEO (.NET Entity Objects)</a></h1>
<p>
"Neo is a framework for .NET developers who want to write enterprise applications with an 
object-based domain model. It is well suited for domain-driven design and agile development."
</p>

<h3>Commercial</h3>

<h1><a href="http://www.llblgen.com/">LLBLGen Pro</a></h1>
<p>
"LLBLGen Pro, the O/R mapper and data-access tier generator for .NET, 
generates a complete data-access tier and business façade/support tier 
for you (in C# or VB.NET), using an existing database schema set. 
In seconds. The generated .NET code is compiler-ready and can, being 
compiled by the .NET C# or VB.NET compiler, be used immediately by other 
applications."
</p>

<h3>Other</h3>

<h1><a href="http://www.mygenerationsoftware.com/">MyGeneration</a></h1>
<p>
"MyGeneration is a Development Tool written in Microsoft .NET. 
MyGeneration generates code from templates by combining either JScript, 
VBScript, DotNetScript C#, or DotNetScript VB.NET with a powerful 
database meta-data API. MyGeneration is 100% FREE  and supports Microsoft 
SQL, Oracle, IBM DB2, MySQL, PostgreSQL, Microsoft Access, FireBird, 
and Interbase."
</p>

